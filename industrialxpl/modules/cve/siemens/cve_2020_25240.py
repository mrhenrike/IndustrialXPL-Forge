"""IXF CVE CVE-2020-25240 — Siemens SINEC NMS (HIGH CVSS 7.5).

Exploit type: Arbitrary file write via upload
CISA Advisory: N/A
Level B: port fingerprint + version context. simulate=True by default.
"""
import socket
from industrialxpl.core.exploit import (
    Exploit, OptBool, OptIP, OptInteger, OptPort, mute,
    print_error, print_info, print_status, print_success, print_warning,
    DestructiveGate,
)

class Exploit(Exploit):
    __info__ = {
        "name": "CVE-2020-25240 Siemens SINEC NMS HIGH",
        "description": "Arbitrary file write via upload. Siemens SINEC NMS. CVSS 7.5 (HIGH).",
        "authors": ("Andre Henrique (@mrhenrike) | Uniao Geek",),
        "references": ("https://nvd.nist.gov/vuln/detail/CVE-2020-25240",),
        "devices": ("Siemens SINEC NMS",),
        "impact": "HIGH",
        "exploit_type": "Arbitrary file write via upload",
        "source_poc": "Static catalog Level B",
        "cve": "CVE-2020-25240",
        "cvss": "7.5",
        "severity": "HIGH",
        "cisa_advisory": "N/A",
        "mitre_techniques": ['T0819', 'T0866'],
        "mitre_tactics": ['Initial Access'],
    }
    target = OptIP("", "Target Siemens device IP")
    port = OptPort(80, "Target service port")
    timeout = OptInteger(5, "Timeout seconds")
    simulate = OptBool(True, "Simulate mode (default: True)")
    destructive = OptBool(False, "Enable real execution gate")

    @mute
    def check(self):
        if not self.target:
            return False
        try:
            s = socket.socket()
            s.settimeout(self.timeout)
            s.connect((self.target, self.port))
            s.close()
            return True
        except Exception:
            return False

    def run(self):
        if not self.target:
            print_error("Set target first.")
            return
        if self.simulate:
            DestructiveGate.print_simulation(
                description="CVE-2020-25240: Fingerprint Siemens SINEC NMS at {}:{}. Arbitrary file write via upload. CVSS 7.5.".format(self.target, self.port),
                mitre_techniques=['T0819', 'T0866'],
            )
            return
        if self.check():
            print_success("Port {} open — Siemens SINEC NMS may be present. CVE-2020-25240 HIGH CVSS 7.5.".format(self.port))
            print_warning("CISA: N/A")
        else:
            print_info("{}:{} not responding.".format(self.target, self.port))
